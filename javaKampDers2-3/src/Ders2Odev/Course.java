package Ders2Odev;

public class Course {
	String name;
	String teacherName;
	String courseDetail;
	int completed;

	
	public Course()
	{
		
	}
	
	public Course(String name, String teacherName, String courseDetail, int completed) {

		this.name = name;
		this.teacherName = teacherName;
		this.courseDetail = courseDetail;
		this.completed = completed;
	}
	
	
	
	

}
